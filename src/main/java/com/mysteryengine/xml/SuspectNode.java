package com.mysteryengine.xml;

public class SuspectNode {
    private String id;
    private String name;
    private String alibi;
    private String facts;
    private String unlockAtScene;

    public String getId() { return id; }
    public void setId(String id) { this.id = id; }
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public String getAlibi() { return alibi; }
    public void setAlibi(String alibi) { this.alibi = alibi; }
    public String getFacts() { return facts; }
    public void setFacts(String facts) { this.facts = facts; }
    public String getUnlockAtScene() { return unlockAtScene; }
    public void setUnlockAtScene(String unlockAtScene) { this.unlockAtScene = unlockAtScene; }
}
